# ==========================================
# Program: Compare Product Prices
# Description: This program compares the prices of two products
# and prints which product is less expensive.
# ==========================================

# Assign prices to products
ferrari = 420000
toyota = 21000

# Compare prices using conditional statements
if ferrari > toyota:
    print("Toyota is less expensive than Ferrari.")
elif ferrari < toyota:
    print("Ferrari is less expensive than Toyota.")
else:
    print("Both products have the same price.")
